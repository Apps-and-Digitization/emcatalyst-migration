def seed_withholding_taxes(db: Session) -> None:
    """Seed withholding tax master data if table is empty."""
    count = db.query(WithHoldingTax).count()
    if count > 0:
        return  # Already seeded

    for with_t, tax_code, section, rate, name in WHT_DATA:
        db.add(WithHoldingTax(
            with_t=with_t,
            tax_code=tax_code,
            section=section,
            rate=rate,
            name=name,
            is_active=True,
        ))
    db.commit()
